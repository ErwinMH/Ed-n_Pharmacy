    public final void Mostrardatos(String valor){
            //Función para llenar la tabla de usuarios desde la base de datos
            MyConnection cc=new MyConnection();
            Connection cn=MyConnection.getConnection();
            RefrescarTabla();
            proveedor.addColumn("Código");
            proveedor.addColumn("Nombre");
            proveedor.addColumn("Dirección");
            
            this.tbl_proveedores.setModel(proveedor);
            String sql;
            
            //Condicional IF que selecciona los proveedores
            if (valor.equals(""))
            {
            sql="SELECT * FROM proveedor";
            }
           
            else{
            sql="SELECT * FROM proveedor WHERE cod_prov='"+valor+"'";
            }
            
            //Arreglo para llenar la tabla de proveedores
            String []datos=new String [3];
            try{
            Statement st=cn.createStatement();
            ResultSet rs=st.executeQuery(sql);
            while(rs.next()){
            datos[0]=rs.getString(1);
            datos[1]=rs.getString(2);
            datos[2]=rs.getString(3);
   
            proveedor.addRow(datos);
            }
            tbl_proveedores.setModel(proveedor);
            }
            //Mensaje de error
            catch(SQLException ex){
            Logger.getLogger(frm_control_proveedores.class.getName()).log(Level.SEVERE,null,ex);
            JOptionPane.showMessageDialog(null, "error "+ex);
            }
            
    }

