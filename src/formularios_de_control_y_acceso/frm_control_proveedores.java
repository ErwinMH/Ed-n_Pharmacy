    private void btn_agregaruserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_agregaruserActionPerformed
        
        //Declaración de variables y sus respectivos campos
        String nom,direc;
        nom = txt_nombreproveedor.getText();
        direc = txt_direccionproveedor.getText();
  
        //Condicional IF para confirmar que hayan campos vacíos
        if ( nom.isEmpty() || direc.isEmpty() ){

            JOptionPane.showMessageDialog(null, "Por favor, llene todos los campos","error", 2);

        }else{
            
            //Declaración de consulta
            PreparedStatement ps;
            String query;
            //Creación de sentencia SQL para insertar los registros en la base de datos
            query = "INSERT INTO `proveedor`(`nom_prov`, `direccion_prov`) VALUES (?,?)";
            try {
                ps = MyConnection.getConnection().prepareStatement(query);

                ps.setString(1, nom);
                ps.setString(2, direc);
                
                //Condicional IF para validad la inserción de los registros
                if(ps.executeUpdate() > 0)
                {
                    JOptionPane.showMessageDialog(null, "Agregado exitosamente!");
                    Mostrardatos("");
                    Limpiar();
                }
            
            //Mensaje de error
            } catch (SQLException ex) {
                Logger.getLogger(frm_control_usuarios.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "error "+ex);
            }
        }
    }//GEN-LAST:event_btn_agregaruserActionPerformed

    private void btn_editaruserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editaruserActionPerformed
        // TODO add your handling code here
        
        //Declaración de variables y sus respectivos campos
        String cod = (String)proveedor.getValueAt(tbl_proveedores.getSelectedRow(),0);
        String nom = txt_nombreproveedor.getText();
        String direc = txt_direccionproveedor.getText();
        
        //Declaración de consulta
        PreparedStatement ps;
        
        //Creación de sentencia para actualizar los registros en la base de datos dado un código de proveedor
        String query = "UPDATE proveedor SET nom_prov=?, direccion_prov=? WHERE cod_prov=?";
        try {
            ps = MyConnection.getConnection().prepareStatement(query);

            ps.setString(3, cod);
            ps.setString(1, nom);
            ps.setString(2, direc);
           

            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Proveedor Modificado");
            Limpiar();
            Mostrardatos("");
        
        //Mensaje de error
        } catch (SQLException ex) {
            Logger.getLogger(frm_control_medicamentos.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "error "+ex);
        }
    }//GEN-LAST:event_btn_editaruserActionPerformed

