    private void btn_agregaruserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_agregaruserActionPerformed
 
        //Declaración de variables y sus respectivos campos
        String nom,user,clave;
        nom = txt_nombre.getText();
        user = txt_user.getText();
        clave = String.valueOf(txt_clave.getPassword());
        int rol = txt_rol.getSelectedIndex();
        
        //Condicional IF que confirma que no hayan campos vacíos
        if ( nom.isEmpty() || user.isEmpty() || clave.isEmpty()){
            
            JOptionPane.showMessageDialog(null, "Por favor, llene todos los campos","error", 2);
            
        }else{
        
        //Declaración de consulta
        PreparedStatement ps;
     
        String query;
        //Creación de sentencia SQL para insertar un registro
        query = "INSERT INTO `usuario`(`nombre_usuario`, `clave`, `nombre_empleado`,`cod_rol`) VALUES (?,?,?,?)";
        try {
            ps = MyConnection.getConnection().prepareStatement(query);
            
            ps.setString(1, user);
            ps.setString(2, clave);
            ps.setString(3, nom);
            ps.setInt(4, rol);
            
            //Condicional IF que confirma la inserción de un usuario
             if(ps.executeUpdate() > 0)
            {
                JOptionPane.showMessageDialog(null, "Agregado exitosamente!");
                Mostrardatos("");
                Limpiar();
            }
        
        //Mensaje de error
        } catch (SQLException ex) {
            Logger.getLogger(frm_control_usuarios.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "error "+ex);
            }
        }
    }//GEN-LAST:event_btn_agregaruserActionPerformed

