    private void btn_editaruserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editaruserActionPerformed
        // TODO add your handling code here:
        
        //Declaración de variables y sus respectivos campos o posición
        String id = (String)usuario.getValueAt(tbl_usuarios.getSelectedRow(),0);
        String nom = txt_nombre.getText();
        String user = txt_user.getText();
        int rol = txt_rol.getSelectedIndex();
        String clave = String.valueOf(txt_clave.getPassword());
      
      
        //Declaración de consulta
        PreparedStatement ps;
        
        //Creación de sentencia SQL para actualizar los datos dado un ID de cliente
        String query = "UPDATE usuario SET nombre_usuario=?, clave=?, nombre_empleado=?, cod_rol=? WHERE ID_usuario=?";
        try {
            ps = MyConnection.getConnection().prepareStatement(query);

            ps.setString(5, id);
            ps.setString(1, user);
            ps.setString(2, clave);
            ps.setString(3, nom);
            ps.setInt(4, rol);
            
        
            ps.executeUpdate();
            //Mensaje que confirma la actualización del usuario
            JOptionPane.showMessageDialog(null, "Usuario Modificado");
            Limpiar();
            Mostrardatos("");
        
        //Mensaje de error
        } catch (SQLException ex) {
            Logger.getLogger(frm_control_medicamentos.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "error "+ex);
        }
    }//GEN-LAST:event_btn_editaruserActionPerformed

